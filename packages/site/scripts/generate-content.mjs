import crypto from 'node:crypto';
import fs from 'node:fs/promises';
import path from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const contentCompiler = require('@elecmonkey/garden-content-compiler');

const CACHE_VERSION = 1;
const SCHEMA_VERSION = 1;
const GENERATED_HEADER = '/* This file is generated by scripts/generate-content.mjs. Do not edit manually. */';

const root = process.cwd();
const postsDirectory = path.join(root, 'src/content/posts');
const generatedDirectory = path.join(root, 'src/generated');
const generatedPostsDirectory = path.join(generatedDirectory, 'posts');
const outputFile = path.join(generatedDirectory, 'content.ts');
const searchIndexFile = path.join(generatedDirectory, 'search-index.json');
const cacheDirectory = path.join(root, '.garden-cache/content');
const cachePostsDirectory = path.join(cacheDirectory, 'posts');
const cacheManifestFile = path.join(cacheDirectory, 'manifest.json');

function normalizeSearchContent(content) {
  return content
    .replace(/```[\s\S]*?```/g, ' ')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/<[^>]+>/g, ' ')
    .replace(/[\[\]()*_>#~-]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function toSearchIndexPost(post) {
  return {
    id: post.id,
    title: post.title,
    date: post.date,
    description: post.description,
    tags: post.tags,
    author: post.author,
    content: normalizeSearchContent(post.content || ''),
  };
}

function normalizePost(post) {
  const {
    monthFolder,
    isDraft,
    isHidden,
    noToc,
    extra = [],
    ...rest
  } = post;

  const extraFields = Object.fromEntries(
    extra.map(({ key, value }) => [key, value]),
  );

  return {
    ...rest,
    monthFolder,
    isDraft,
    isHidden,
    ...(noToc === undefined ? {} : { no_toc: noToc }),
    ...extraFields,
  };
}

function isDraftMarkdown(fileName) {
  return fileName.endsWith('.md') && fileName.startsWith('draft-');
}

function isPostMarkdown(fileName) {
  return fileName.endsWith('.md') && !isDraftMarkdown(fileName);
}

function fileHash(source) {
  return `sha256:${crypto.createHash('sha256').update(source).digest('hex')}`;
}

function toPosixPath(value) {
  return value.split(path.sep).join('/');
}

function toSourceKey(filePath) {
  return toPosixPath(path.relative(root, filePath));
}

function toCacheFile(slug) {
  return `posts/${slug}.json`;
}

function toGeneratedPostFile(slug) {
  return path.join(generatedPostsDirectory, `${slug}.ts`);
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function readJsonFile(filePath, fallback) {
  try {
    return JSON.parse(await fs.readFile(filePath, 'utf8'));
  } catch {
    return fallback;
  }
}

async function writeFileIfChanged(filePath, content) {
  try {
    const current = await fs.readFile(filePath, 'utf8');
    if (current === content) {
      return false;
    }
  } catch {
    // New file.
  }

  await fs.mkdir(path.dirname(filePath), { recursive: true });
  await fs.writeFile(filePath, content);
  return true;
}

async function removeIfExists(filePath) {
  try {
    await fs.rm(filePath, { force: true });
  } catch {
    // Nothing to remove.
  }
}

async function scanPostFiles(directory) {
  const files = [];
  const entries = await fs.readdir(directory, { withFileTypes: true });

  for (const entry of entries) {
    const entryPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...await scanPostFiles(entryPath));
      continue;
    }

    if (entry.isFile() && isPostMarkdown(entry.name)) {
      files.push(entryPath);
    }
  }

  return files.sort().reverse();
}

function createEmptyManifest() {
  return {
    version: CACHE_VERSION,
    compilerVersion: contentCompiler.version(),
    schemaVersion: SCHEMA_VERSION,
    posts: {},
  };
}

function isUsableManifest(manifest) {
  return manifest
    && manifest.version === CACHE_VERSION
    && manifest.compilerVersion === contentCompiler.version()
    && manifest.schemaVersion === SCHEMA_VERSION
    && manifest.posts
    && typeof manifest.posts === 'object';
}

async function readCachePost(cacheFile) {
  const cachePath = path.join(cacheDirectory, cacheFile);
  return readJsonFile(cachePath, undefined);
}

async function writeCachePost(cacheFile, post) {
  const cachePath = path.join(cacheDirectory, cacheFile);
  await writeFileIfChanged(cachePath, `${JSON.stringify(post)}\n`);
}

async function buildPostsWithCache() {
  const cachedManifest = await readJsonFile(cacheManifestFile, undefined);
  const previousManifest = isUsableManifest(cachedManifest) ? cachedManifest : createEmptyManifest();
  const nextManifest = createEmptyManifest();
  const postFiles = await scanPostFiles(postsDirectory);
  const posts = [];
  let compiledCount = 0;
  let reusedCount = 0;

  await fs.mkdir(cachePostsDirectory, { recursive: true });

  for (const filePath of postFiles) {
    const source = await fs.readFile(filePath);
    const hash = fileHash(source);
    const sourceKey = toSourceKey(filePath);
    const monthFolder = path.basename(path.dirname(filePath));
    const previousEntry = previousManifest.posts[sourceKey];
    let post;

    if (previousEntry?.hash === hash) {
      post = await readCachePost(previousEntry.cacheFile);
    }

    if (post) {
      reusedCount += 1;
    } else {
      const compiledPost = contentCompiler.compilePost(filePath, monthFolder);
      if (!compiledPost) {
        continue;
      }
      post = normalizePost(compiledPost);
      compiledCount += 1;
    }

    const cacheFile = toCacheFile(post.id);
    await writeCachePost(cacheFile, post);

    nextManifest.posts[sourceKey] = {
      slug: post.id,
      monthFolder,
      hash,
      cacheFile,
    };
    posts.push({ ...post, sourceKey });
  }

  const activeCacheFiles = new Set(
    Object.values(nextManifest.posts).map((entry) => entry.cacheFile),
  );

  for (const entry of Object.values(previousManifest.posts)) {
    if (!activeCacheFiles.has(entry.cacheFile)) {
      await removeIfExists(path.join(cacheDirectory, entry.cacheFile));
    }
  }

  posts.sort((a, b) => b.date.localeCompare(a.date));
  for (const post of posts) {
    delete post.sourceKey;
  }
  await writeFileIfChanged(cacheManifestFile, `${JSON.stringify(nextManifest, null, 2)}\n`);

  return { posts, compiledCount, reusedCount };
}

function createPostModule(post) {
  return `${GENERATED_HEADER}\n\n` +
    `import type { PostData } from '@/lib/api';\n\n` +
    `export const post = ${JSON.stringify(post, null, 2)} satisfies PostData;\n`;
}

function createContentModule(posts) {
  const imports = posts
    .map((post, index) => `import { post as post${index} } from './posts/${post.id}';`)
    .join('\n');
  const postList = posts.map((_post, index) => `  post${index},`).join('\n');

  return `${GENERATED_HEADER}\n\n` +
    `import type { PostData } from '@/lib/api';\n` +
    `${imports}\n\n` +
    `export const generatedPosts = [\n${postList}\n] satisfies PostData[];\n\n` +
    `export const generatedPublicPosts = generatedPosts.filter((post) => !post.isHidden);\n`;
}

async function cleanupStaleGeneratedPosts(activeSlugs) {
  if (!await pathExists(generatedPostsDirectory)) {
    return;
  }

  const entries = await fs.readdir(generatedPostsDirectory, { withFileTypes: true });
  await Promise.all(entries.map(async (entry) => {
    if (!entry.isFile() || !entry.name.endsWith('.ts')) {
      return;
    }

    const slug = entry.name.slice(0, -'.ts'.length);
    if (!activeSlugs.has(slug)) {
      await removeIfExists(path.join(generatedPostsDirectory, entry.name));
    }
  }));
}

async function writeGeneratedFiles(posts) {
  await fs.mkdir(generatedPostsDirectory, { recursive: true });

  const activeSlugs = new Set(posts.map((post) => post.id));
  await cleanupStaleGeneratedPosts(activeSlugs);

  let changedPostModules = 0;
  for (const post of posts) {
    const changed = await writeFileIfChanged(toGeneratedPostFile(post.id), createPostModule(post));
    if (changed) {
      changedPostModules += 1;
    }
  }

  const publicPosts = posts.filter((post) => !post.isHidden);
  const searchIndex = publicPosts.map(toSearchIndexPost);
  const contentChanged = await writeFileIfChanged(outputFile, createContentModule(posts));
  const searchChanged = await writeFileIfChanged(searchIndexFile, JSON.stringify(searchIndex));

  return { changedPostModules, contentChanged, searchChanged };
}

const { posts, compiledCount, reusedCount } = await buildPostsWithCache();
const { changedPostModules, contentChanged, searchChanged } = await writeGeneratedFiles(posts);

console.log(
  `generated ${posts.length} posts ` +
  `(${compiledCount} compiled, ${reusedCount} cached, ${changedPostModules} post modules changed) ` +
  `to ${path.relative(root, outputFile)} and ${path.relative(root, searchIndexFile)} ` +
  `with content compiler ${contentCompiler.version()}`,
);

if (contentChanged || searchChanged) {
  console.log(`updated indexes: content=${contentChanged}, search=${searchChanged}`);
}
