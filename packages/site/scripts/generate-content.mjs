import fs from 'node:fs/promises';
import path from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const contentCompiler = require('@elecmonkey/garden-content-compiler');

const root = process.cwd();
const postsDirectory = path.join(root, 'src/content/posts');
const outputFile = path.join(root, 'src/generated/content.ts');
const searchIndexFile = path.join(root, 'src/generated/search-index.json');


function normalizeSearchContent(content) {
  return content
    .replace(/```[\s\S]*?```/g, ' ')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/<[^>]+>/g, ' ')
    .replace(/[\[\]()*_>#~-]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function toSearchIndexPost(post) {
  return {
    id: post.id,
    title: post.title,
    date: post.date,
    description: post.description,
    tags: post.tags,
    author: post.author,
    content: normalizeSearchContent(post.content || ''),
  };
}

function normalizePost(post) {
  const {
    monthFolder,
    isDraft,
    isHidden,
    noToc,
    extra = [],
    ...rest
  } = post;

  const extraFields = Object.fromEntries(
    extra.map(({ key, value }) => [key, value]),
  );

  return {
    ...rest,
    monthFolder,
    isDraft,
    isHidden,
    ...(noToc === undefined ? {} : { no_toc: noToc }),
    ...extraFields,
  };
}

const manifest = contentCompiler.compilePosts(postsDirectory);
const posts = manifest.posts.map(normalizePost);
const publicPosts = manifest.publicPosts.map(normalizePost);

const source = `/* This file is generated by scripts/generate-content.mjs. Do not edit manually. */\n\n` +
  `import type { PostData } from '@/lib/api';\n\n` +
  `export const generatedPosts = ${JSON.stringify(posts, null, 2)} satisfies PostData[];\n\n` +
  `export const generatedPublicPosts = ${JSON.stringify(publicPosts, null, 2)} satisfies PostData[];\n`;

await fs.mkdir(path.dirname(outputFile), { recursive: true });
const searchIndex = publicPosts.map(toSearchIndexPost);

await fs.writeFile(outputFile, source);
await fs.writeFile(searchIndexFile, JSON.stringify(searchIndex));
console.log(`generated ${posts.length} posts to ${path.relative(root, outputFile)} and ${path.relative(root, searchIndexFile)} with content compiler ${contentCompiler.version()}`);
