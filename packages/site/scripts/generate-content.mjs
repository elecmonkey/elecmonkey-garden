import fs from 'node:fs/promises';
import path from 'node:path';
import matter from 'gray-matter';

const root = process.cwd();
const postsDirectory = path.join(root, 'src/content/posts');
const outputFile = path.join(root, 'src/generated/content.ts');
const draftPrefix = 'draft-';
const hiddenPrefix = 'hidden-';

function isDraftFile(fileName) {
  return fileName.startsWith(draftPrefix);
}

function isHiddenFile(fileName) {
  return fileName.startsWith(hiddenPrefix);
}

function extractIdFromFileName(fileName) {
  let id = fileName.replace(/\.md$/, '');
  if (id.startsWith(draftPrefix)) id = id.slice(draftPrefix.length);
  if (id.startsWith(hiddenPrefix)) id = id.slice(hiddenPrefix.length);
  return id;
}

async function getMonthFolders() {
  const entries = await fs.readdir(postsDirectory, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isDirectory())
    .filter((entry) => /^\d{6}$/.test(entry.name))
    .map((entry) => entry.name)
    .sort()
    .reverse();
}

async function readPostsFromMonth(monthFolder) {
  const monthPath = path.join(postsDirectory, monthFolder);
  const fileNames = await fs.readdir(monthPath);

  return Promise.all(
    fileNames
      .filter((fileName) => fileName.endsWith('.md'))
      .filter((fileName) => !isDraftFile(fileName))
      .map(async (fileName) => {
        const fullPath = path.join(monthPath, fileName);
        const fileContents = await fs.readFile(fullPath, 'utf8');
        const { data, content } = matter(fileContents);

        return {
          id: extractIdFromFileName(fileName),
          content,
          monthFolder,
          isDraft: false,
          isHidden: isHiddenFile(fileName),
          ...data,
        };
      }),
  );
}

const monthFolders = await getMonthFolders();
const posts = (await Promise.all(monthFolders.map(readPostsFromMonth)))
  .flat()
  .sort((a, b) => (a.date < b.date ? 1 : -1));

const publicPosts = posts.filter((post) => !post.isHidden);

const source = `/* This file is generated by scripts/generate-content.mjs. Do not edit manually. */\n\n` +
  `import type { PostData } from '@/lib/api';\n\n` +
  `export const generatedPosts = ${JSON.stringify(posts, null, 2)} satisfies PostData[];\n\n` +
  `export const generatedPublicPosts = ${JSON.stringify(publicPosts, null, 2)} satisfies PostData[];\n`;

await fs.mkdir(path.dirname(outputFile), { recursive: true });
await fs.writeFile(outputFile, source);
console.log(`generated ${posts.length} posts to ${path.relative(root, outputFile)}`);
